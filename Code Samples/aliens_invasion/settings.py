class Settings:
    """ A class to store all settings for Alien Invasion """

    def __init__(self):
        """ Initialize game's settings """
        # Screen settings
        self.screen_width = 1200
        self.screen_height = 800
        self.bg_color = (50, 18, 122)

        self.ship_height = 82
        self.ship_width = 60
        self.ship_rotation = 90
